//! Precomputed values for regular polygons.
//! Trig functions aren't const, so we have to compute these by hand.
//! These are computed such that polygons with odd number of vertices
//! have their symmetry horizontally across the y-axis
//! and a flat edge on the bottom,
//! hence angle 0 corresponds to -y instead of the conventional +x.
//!
//! These were computed with ad-hoc Nushell commands,
//! but formulas are given for future reference where conveniently available.

use std::f64::consts::{FRAC_1_SQRT_2, SQRT_2};

use crate::math::{DVec2, UnitDVec2};

/// Number of regular polygons we have precomputed values for;
/// for polygons with more edges (which are unlikely to exist)
/// we fall back to computing values at runtime
/// (which is expensive and should be avoided)
